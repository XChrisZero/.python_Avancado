# ao usar o "Ctrl + c" e "Ctrl + v" em qualquer parte da linha ele copia e cola a linha toda, mesmo sem grifar tudo
print (12, 34, sep = '...') #sep, comando para definir o separador padrão podendo ser o que quiser
print (12, 34, sep = ",,,")# o SEP, pode ser usado tanto com aspas simples quanto dupla

print (8, 'D', sep = "===", end = "qualquer coisa  ")# end, comando pará adicionar algo no final do print

print (56, 78, end = '\r\n') #por padrão esse comando end, ja está ai com essa quebra de linha (\r\n >>> CRLF)
print (9, 10, end = "\n") #tambem funcionará para quebrar a linha. (\N >>> LF)


